package controlstatements;

public class PerfectSquare 
{
   public static void main(String[]args)
   {
	    int n=12;
	    int i=1;
	    
	    	while(i*i<n)
	    	{
	    		i++;
	    	}
	    	if(i*i==n)
	    	{
	    		System.out.println(n+":it is a perfect");
	    	}
	    	else
	    	{
	    		System.out.println(n+":it is a  imperfect");

	    	}
	    	
	    	
	       
   }
}
